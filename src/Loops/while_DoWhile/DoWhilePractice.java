public class DoWhilePractice {
    public static void main(String[] args) {
        // show your age 21 to 26
        int age = 21;
        do {
            System.out.println("Your age is: " + age);
            age++;

        } while (age < 15);  // this is the only difference with: while and do-while loops
                             // in do-while loop will still print out 1sk block of code
                             // at least once even if while condition is false

        }

    }
